        Parce qu’il connaît les lieux « par cœur », qu’il les a parcourus une multitude de fois sans affronter aucune difficulté, qu’il se souvient d’en avoir repéré tous les pièges…, le conducteur peut s’y sentir invincible. A tort.

        Le risque, c’est de relâcher son attention : quitter la route des yeux, se laisser distraire dangereusement par son téléphone, se perdre dans ses pensées…
        Pire. Le conducteur peut prendre des risques… qu’il ne tenterait pas ailleurs. Par exemple, rouler trop vite (parti à la dernière minute, il est pressé), franchir une ligne continue (pour dépasser un tracteur qui « se traîne »)…
        Même si l’itinéraire est familier, les dangers ne disparaissent pas pour autant. Savoir où se trouve un virage délicat à négocier n’élimine pas l’obstacle imprévu : un animal traversant la route, un véhicule débouchant d’une petite route au sommet d’une côte, une moto qui semble surgir subitement derrière soi, etc
